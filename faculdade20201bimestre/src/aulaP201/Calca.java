package aulaP201;

public class Calca {
	String marca;
	String cor;
	double valor;
	String tpCalca; // boca de sino, bag, bombacha
	boolean temCinto;
	String tamanho;
	@Override
	public String toString() {
		return "Calca [marca=" + this.marca + ", cor=" + this.cor + ", valor=" + this.valor + ", tpCalca=" + this.tpCalca + ", temCinto="
				+ this.temCinto + ", tamanho=" + this.tamanho + "]";
	}
	
	
}
